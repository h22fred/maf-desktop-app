package utils.form;

import play.data.validation.Constraints;

import javax.validation.Constraint;
import javax.validation.Payload;
import java.lang.annotation.Retention;
import java.lang.annotation.Target;

import static java.lang.annotation.ElementType.FIELD;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

@Target({FIELD})
@Retention(RUNTIME)
@Constraint(validatedBy = ConditionalRequiredValidator.class)
@play.data.Form.Display(name="conditional.constraint.required")
public @interface ConditionalRequired {
    String message() default Constraints.RequiredValidator.message;
    Class<?>[] groups() default {};
    Class<? extends Payload>[] payload() default {};
    String value();
}